// 根据课程类型返回领域化系统提示词
export function getDomainPrompt(theme?: string): string {
  if (!theme) return getDefaultPrompt();

  // 优先匹配更具体的类别
  const codeKeywords = ["Scratch", "Python", "C++", "编程"];
  const robotKeywords = ["WEDO", "SPIKE", "机器人"];
  const legoKeywords = ["大颗粒", "小颗粒", "BricQ", "乐高", "积木"];

  if (codeKeywords.some(k => theme.includes(k))) return CODE_PROGRAMMING_PROMPT;
  if (robotKeywords.some(k => theme.includes(k))) return ROBOT_PROGRAMMING_PROMPT;
  if (legoKeywords.some(k => theme.includes(k))) return LEGO_BUILDING_PROMPT;

  return getDefaultPrompt();
}

// 从领域 Prompt 中提取评价维度部分（最后200字，通常是评价维度和格式要求）
export function extractEvaluationDimensions(domainPrompt: string): string {
  if (domainPrompt.length <= 200) return domainPrompt;
  return domainPrompt.slice(-200);
}
