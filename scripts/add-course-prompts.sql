-- ============================================
-- 教学反馈系统 - 课程阶段专属 AI 提示词迁移脚本
-- 在 Supabase Dashboard -> SQL Editor 中执行
-- ============================================
-- 说明：
--   1. 创建 course_prompts 表（21 个课程阶段的专属提示词）
--   2. 启用 RLS：所有人可读，仅 admin（service_role）可写
--   3. 插入 21 个课程阶段的默认深度优化提示词
-- ============================================

-- ============================================
-- 1. course_prompts 表 DDL
-- ============================================

CREATE TABLE IF NOT EXISTS course_prompts (
  id VARCHAR(36) PRIMARY KEY DEFAULT gen_random_uuid(),
  stage_code VARCHAR(50) NOT NULL UNIQUE,
  system_prompt TEXT,
  report_structure TEXT,
  word_limit VARCHAR(20),
  is_active BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS course_prompts_stage_code_idx ON course_prompts(stage_code);
CREATE INDEX IF NOT EXISTS course_prompts_active_idx ON course_prompts(is_active);

-- ============================================
-- 2. 启用 RLS
-- ============================================

ALTER TABLE course_prompts ENABLE ROW LEVEL SECURITY;

-- service_role 完整访问（用于 API 路由）
CREATE POLICY "service_role_full_access_course_prompts" ON course_prompts
  FOR ALL USING (true) WITH CHECK (true);

-- anon 只读（前端可加载提示词列表）
CREATE POLICY "anon_read_course_prompts" ON course_prompts
  FOR SELECT USING (true);

-- ============================================
-- 3. 自动更新 updated_at 触发器
-- ============================================

CREATE OR REPLACE FUNCTION update_course_prompts_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS course_prompts_updated_at_trigger ON course_prompts;
CREATE TRIGGER course_prompts_updated_at_trigger
  BEFORE UPDATE ON course_prompts
  FOR EACH ROW
  EXECUTE FUNCTION update_course_prompts_updated_at();

